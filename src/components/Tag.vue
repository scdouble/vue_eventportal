<template>
  <button class="btn-tag" @click="showEventByTag(tagName)">
    #{{ tagName }}
  </button>
</template>

<script>
import { useRouter, useRoute } from "vue-router";

export default {
  name: "Tag",
  props: {
    tagName: String,
  },
  setup() {
    const router = useRouter();

    function showEventByTag(tagName) {
      router.push({
        name: "eventsbytag",
        path: "/tag",
        params: { name: tagName },
      });
    }
    return { showEventByTag };
  },
};
</script>

